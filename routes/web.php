    Route::delete('site-setings/destroy', 'SiteSetingsController@massDestroy')->name('site-setings.massDestroy');
    Route::post('site-setings/media', 'SiteSetingsController@storeMedia')->name('site-setings.storeMedia');
    Route::post('site-setings/ckmedia', 'SiteSetingsController@storeCKEditorImages')->name('site-setings.storeCKEditorImages');
    Route::resource('site-setings', 'SiteSetingsController');

    // Bookings
    Route::resource('bookings', 'BookingsController', ['except' => ['create', 'store', 'edit', 'update', 'destroy']]);

    // Subscribers
    Route::resource('subscribers', 'SubscribersController', ['except' => ['create', 'store', 'edit', 'update', 'destroy']]);

